#' Square a number
#' 
#' Takes in any numeric value and squares it.
#' @param x A numeric value to be square
#' @return The square of the input
#' @export
square <- function(x){
  return(x^2)
}

#' Cube a number
#' @param x Number to be cubed
#' @return The cube of the input
#' @export
cube <- function(x){
  return(x^3)
}